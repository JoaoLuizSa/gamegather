<h2 style="margin-top: 40px;">Mercado</h2>
<p class="paragrafs">
	Analisando o mercado de games e principalmente o dos eSports, encontramos uma medida interessante que o jogo Overwatch, lançado em 24 de maio de 2016 e desenvolvido pela Blizzard Entertainment. No decorrer do tempo, Overwatch começou a ser um grande alvo da toxicidade, dia após dia, os servidores da Blizzard recebiam mais e mais denuncias de má conduta e assédio e perceberam que isso podia sim ser o motivo da morte do game.

	Em uma atualização recente, jogadores se depararam com uma nova mecânica atribuída ao game, conhecida com "Endorsement", um sistema de indicações que iria trazer reconhecimento aos incriveis jogadores que esse game estava sendo capaz de criar com todo seu sucesso.</p>

