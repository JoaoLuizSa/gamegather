<h2>Como isso ajudou a comunidade?</h2>
<p class="paragrafs">
	Os desenvolvedores conseguiram perceber o principalmente problema causador da toxicidade nos jogos: o individualismo.
Muitos jogos tentam corrigir o problema da toxicidade com atitudes cooperativas, como o caso do League Of Legends, lançado em 27 de outubro de 2009 e desenvolvido pela Riot Games, que os desenvolvedores pensaram na divisao dos jogadores em dois grandes grupos, onde o grupo que tivesse menos denuncias de má conduta, ganhariam uma recompensa, mas obviamente, nao foi tão efetivo devido ao tamanho da comunidade e a questão do jogador toxico nao ligar para o outro.
O caso do Overwatch com seu sistema de Endorsement é diferente, primeiramente que a recompensa só consegue ser alcançada individualmente, logo você não se sente carregando nenhum tóxico nas costas e segundo que existe realmente uma penalidade contínua ao mal jogador. O jogador cada vez mais pega jogadores semelhantes a ele devido ao seu nível de indicação.
Além de existir todo um trabalho emocional por trás, no final de um você receber uma grande quantidade de votos lhe traz um sentimento de interaçao e importancia para time, fazendo assim com que o jogador tenha mais confiança nas suas abilidades e tenha prazer em jogar com diferentes pessoas. Cabe ressaltar que o jogar quando recebe um voto, ele é notificado instantaneamente com um som e com o tipo de categoria que foi indicado, sendo isso mostrado no canto inferior esquerdo, onde se localiza o chat do game.</p>

