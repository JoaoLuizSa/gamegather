A recompensa além de ser otimas experiencias durante os jogos, sao também a obtenção de "Lootboxes", caixas que concedem ao jogador 4 itens aleatórios, que variam em sprays, visuais, falas, e dinheiro virtual para ser gasto na compra de um item especifico anterior. A cada nivel elevado de suas indicações você recebe uma lootbox por simplesmente ser um bom jogador.</p>

